#union of sets
set1 = {1,2,3}
set2 = {2,3,4}
print(set1.union(set2))
print(set1)
print(set2)

#intersection of sets
set1 = {1,2,3}
set2 = {2,3,4}
print(set1.intersection(set2))
print(set1)
